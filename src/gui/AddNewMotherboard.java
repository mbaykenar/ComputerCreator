    public void initComboBoxes(){
        
        jComboBox1.removeAllItems();
        jComboBox2.removeAllItems();
        jComboBox3.removeAllItems();
        jComboBox4.removeAllItems();
        jComboBox5.removeAllItems();
        
        for(int i = 0; i < cpus.size(); i++){
            jComboBox1.addItem(cpus.get(i).getModel() + ": " + cpus.get(i).getVendor());
        }
        
        for(int i = 0; i < gpus.size(); i++){
            jComboBox2.addItem(gpus.get(i).getModel() + ": " + gpus.get(i).getVendor());
        }
        
        for(int i = 0; i < memories.size(); i++){
            jComboBox3.addItem(memories.get(i).getModel() + ": " + memories.get(i).getVendor());
        }
        
        for(int i = 0; i < hdds.size(); i++){
            jComboBox4.addItem(hdds.get(i).getModel() + ": " + hdds.get(i).getVendor());
        }
        
        for(int i = 0; i < ssds.size(); i++){
            jComboBox5.addItem(ssds.get(i).getModel() + ": " + ssds.get(i).getVendor());
        }
        
    }

