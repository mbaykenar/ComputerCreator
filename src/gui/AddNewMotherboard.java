    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        try{
            String model = jTextField1.getText();
            String vendor = jTextField2.getText();
            String socket = jTextField3.getText();
            String busInterface = jTextField4.getText();
            int memorySlots = Integer.parseInt(jTextField5.getText());
            int pciSlots = Integer.parseInt(jTextField6.getText());
            int pciESlots = Integer.parseInt(jTextField7.getText());
            double frequency = Double.parseDouble(jTextField8.getText());
            double price = Double.parseDouble(jTextField9.getText());
            
            Motherboard m = new Motherboard(socket, memorySlots, pciSlots, pciESlots, busInterface, frequency, model, vendor, price);
            m.insertMotherboard();
            m.retrieveMotherboard(m.getModel()); //to get id generated by the DBMS
            
            m.setMatchedCpuIDs(cpusToAdd);
            m.storeCpuMatches();
            
            m.setMatchedGpuIDs(gpusToAdd);
            m.storeGpuMatches();
            
            m.setMatchedMemoryIDs(memoriesToAdd);
            m.storeMemoryMatches();
            
            m.setMatchedHddIDs(hddsToAdd);
            m.storeHddMatches();
            
            m.setMatchedSsdIDs(ssdsToAdd);
            m.storeSsdMatches();
            
            this.dispose();
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Check information you have entered!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

